package day18;

public class EvenNumberForLoop {
    public static void main(String[] args) {

        for (int i = 0; i <= 20; i += 2)
            System.out.println("i = " + i);
        System.out.println("----------------");
        for (int j = 0; j <=20 ; j+=3)
            System.out.println("j = " + j);
    }
}
