package day06;

public class CompoundOperator {
    public static void main(String[] args) {
        int studentOnline = 263;
        studentOnline += 5;
        System.out.println("Current number 0f Online Students is "+ studentOnline);

        studentOnline -= 3;

        System.out.println("3 students left from class and Current number 0f Online Students is "+ studentOnline);
        studentOnline = studentOnline * 2;

        System.out.println("Students are doubled and Current number 0f Online Students is "+ studentOnline);




    }
}
