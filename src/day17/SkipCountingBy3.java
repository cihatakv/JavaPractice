package day17;

public class SkipCountingBy3 {
    public static void main(String[] args) {

        //Skip counting by 3 from 0 till 30
        // 3 6 9 12 15 18 21 14 17 30

        int counter = 0;

        while (counter < 30) {
            counter += 3;
            System.out.println(counter + " ");

        }




    }


}
