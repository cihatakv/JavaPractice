package day31;

public class MethodPractice2 {
    public static void main(String[] args) {
        count1To10();
        count1To10();
    }

    public static void count1To10() {
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println("\n");
    }

}
